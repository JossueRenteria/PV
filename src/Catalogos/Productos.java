    public void cargarTabla(){
        tableProductos.getColumnModel().getColumn(0).setPreferredWidth(20);
        tableProductos.getColumnModel().getColumn(1).setPreferredWidth(20);
        tableProductos.getColumnModel().getColumn(2).setPreferredWidth(150);
        
        DefaultTableModel gridProductos = (DefaultTableModel) tableProductos.getModel();
        gridProductos.setNumRows(0);
        
        try{
            while(conexionProductos.datos.next()){
                gridProductos.addRow(new Object[]{conexionProductos.datos.getString("id"),
                                                  conexionProductos.datos.getString("codigo"),
                                                  conexionProductos.datos.getString("nombre"),
                                                  conexionProductos.datos.getString("precio_costo"),
                                                  conexionProductos.datos.getString("precio_venta"),
                                                  conexionProductos.datos.getString("stock"),
                                                  conexionProductos.datos.getString("Linea_id"),
                                                  conexionProductos.datos.getString("Proveedor_id")});
                                                                                          
            }
        }
        catch(SQLException Error){
            JOptionPane.showMessageDialog(null, "Error", Error.getMessage(), JOptionPane.ERROR_MESSAGE);
        }
    }
