    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        // Codigo necesario para eliminar registros:
        Producto dlg;
        dlg = new Producto();
        
        dlg.configurarPara("ELIMINAR");
        dlg.setTitle("Eliminar articulo");
        dlg.enlace = conexionProductos;
        dlg.padre = this;
        
         //Recuperamos el registro activo en el JTable
        int registroActivo = tableProductos.getSelectedRow();
        
        if(registroActivo==-1){
            JOptionPane.showMessageDialog(this, "No se ha seleccionado ningun registro", 
                                          "Mensaje", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Vector registro = null;
        DefaultTableModel modelo;
        
        modelo = (DefaultTableModel)tableProductos.getModel();
        
        //Extrar el vector del JTable
        registro = (Vector)modelo.getDataVector().elementAt(registroActivo);
        dlg.cargarCampos(registro.elementAt(0).toString());
                        
        dlg.setVisible(true);
        dlg.setLocationRelativeTo(this);
    }//GEN-LAST:event_btn_eliminarActionPerformed

