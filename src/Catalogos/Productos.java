    private void btn_anteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_anteriorActionPerformed
        // ir al registro anterior:
        int registros = 0;
        registros = tableProductos.getRowCount();
        if(registros == 0){
            JOptionPane.showMessageDialog(null, "No hay registros capturados");
            return;
        }
        registros = tableProductos.getSelectedRow();
        if(registros == 0){
            JOptionPane.showMessageDialog(null, "No es posible desplazarse más");
        }else{
            tableProductos.changeSelection(registros-1, 0, false, false);
        }
    }//GEN-LAST:event_btn_anteriorActionPerformed

    private void btn_siguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_siguienteActionPerformed
        // ir al siguiente registro: aqui me quedé
        int registros = 0;
        registros = tableProductos.getRowCount();
        if(registros == 0){
            JOptionPane.showMessageDialog(null, "No hay registros capturados");
            return;
        }
        registros = tableProductos.getSelectedRow();
        if(registros == (tableProductos.getRowCount()-1)){
            JOptionPane.showMessageDialog(null, "No es posible desplazarse más");
        }else{
            tableProductos.changeSelection(registros+1, 0, false, false);
        }
    }//GEN-LAST:event_btn_siguienteActionPerformed

